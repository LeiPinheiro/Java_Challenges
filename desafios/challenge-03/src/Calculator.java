public class Calculator {

    public void algotitmo(int A, int B) {
        if (A == B) {
            int C = A + B;

            System.out.println("O valor de A + B sao iguais aqui esta a soma: " + C);
        } else {
            int C = A * B;
            System.out.println("O valor de A + B nao sao iguais, aqui esta o valor multiplicado: " + C);
        }
    }
}
